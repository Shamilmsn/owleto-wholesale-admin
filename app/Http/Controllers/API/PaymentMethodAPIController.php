       $paymentMethods = PaymentMethod::where('is_active', true);

       if($request->is_delivery_pickup){
           $paymentMethods = $paymentMethods->where('id', PaymentMethod::PAYMENT_METHOD_RAZORPAY);
       }

        $paymentMethods = $paymentMethods->get();

        return $this->sendResponse($paymentMethods->toArray(),'Payment Methods retrieved successfully');
    }

