     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $deliveryTypes = DeliveryType::query()
            ->with('sectorDeliveryTypes');
//
        $sectorId = $request->sector_id;

        if($sectorId){
            $deliveryTypes = $deliveryTypes->whereHas('sectorDeliveryTypes', function ($query) use($sectorId) {
                $query->where('field_id', $sectorId);
            });
        }

        $deliveryTypes = $deliveryTypes->get();

        return $this->sendResponse($deliveryTypes,'Delivery Type retrieved successfully');
    }

